def postprocess(
    prediction, num_classes, conf_thre=0.7, nms_thre=0.45, class_agnostic=False
):
    box_corner = prediction.new(prediction.shape)
    box_corner[:, :, 0] = prediction[:, :, 0] - prediction[:, :, 2] / 2
    box_corner[:, :, 1] = prediction[:, :, 1] - prediction[:, :, 3] / 2
    box_corner[:, :, 2] = prediction[:, :, 0] + prediction[:, :, 2] / 2
    box_corner[:, :, 3] = prediction[:, :, 1] + prediction[:, :, 3] / 2
    prediction[:, :, :4] = box_corner[:, :, :4]

    output = [None for _ in range(len(prediction))]
    for i, image_pred in enumerate(prediction):

        # If none are remaining => process next image
        if not image_pred.size(0):
            continue
        # Get score and class with highest confidence
        class_conf, class_pred = torch.max(
            image_pred[:, 5 : 5 + num_classes], 1, keepdim=True
        )

        conf_mask = (image_pred[:, 4] * class_conf.squeeze() >= conf_thre).squeeze()
        # Detections ordered as (x1, y1, x2, y2, obj_conf, class_conf, class_pred)
        detections = torch.cat((image_pred[:, :5], class_conf, class_pred.float()), 1)
        detections = detections[conf_mask]
        if not detections.size(0):
            continue

        if class_agnostic:
            nms_out_index = torchvision.ops.nms(
                detections[:, :4],
                detections[:, 4] * detections[:, 5],
                nms_thre,
            )
        else:
            nms_out_index = torchvision.ops.batched_nms(
                detections[:, :4],
                detections[:, 4] * detections[:, 5],
                detections[:, 6],
                nms_thre,
            )

        detections = detections[nms_out_index]
        if output[i] is None:
            output[i] = detections
        else:
            output[i] = torch.cat((output[i], detections))

    return output


class Predictor(object):
    def __init__(
        self,
        model,
        num_classes,
        confthre,
        nmsthre,
        test_size,
        cls_names=YOEO_CLASSES,
    ):
        self.model = model
        self.cls_names = cls_names
        self.num_classes = num_classes
        self.confthre = confthre
        self.nmsthre = nmsthre
        self.test_size = test_size
        self.preproc = ValTransform(legacy=False)

    def inference(self, img):
        img_info = {"id": 0}
        if isinstance(img, str):
            img_info["file_name"] = os.path.basename(img)
            img = cv2.imread(img)
        else:
            img_info["file_name"] = None

        height, width = img.shape[:2]
        img_info["height"] = height
        img_info["width"] = width
        img_info["raw_img"] = img

        ratio = min(self.test_size[0] / img.shape[0], self.test_size[1] / img.shape[1])
        img_info["ratio"] = ratio

        img, _ = self.preproc(img, None, self.test_size)
        img = torch.from_numpy(img).unsqueeze(0)
        img = img.float()
        # print(img.shape)

        with torch.no_grad():
            t0 = time.time()
            outputs = self.model(img)
            # if self.decoder is not None:
            #     outputs = self.decoder(outputs, dtype=outputs.type())
            outputs = postprocess(
                outputs,
                self.num_classes,
                self.confthre,
                self.nmsthre,
                class_agnostic=True,
            )
        return outputs, img_info

    def visual(self, output, img_info, cls_conf=0.35):
        ratio = img_info["ratio"]
        img = img_info["raw_img"]
        if output is None:
            return img, (torch.empty(0), torch.empty(0), torch.empty(0))
        output = output.cpu()

        bboxes = output[:, 0:4]

        # preprocessing: resize
        bboxes /= ratio

        cls = output[:, 6]
        scores = output[:, 4] * output[:, 5]

        vis_res = vis(img, bboxes, scores, cls, cls_conf, self.cls_names)
        return vis_res, (bboxes, cls, scores)


def video_predict(
    video_file,
    out_path,
    model,
    num_classes,
    confthre,
    nmsthre,
    test_size,
    YOEO_CLASSES,
    ifps,
    verbose=False,
):

    predictor = Predictor(
        model, num_classes, confthre, nmsthre, test_size, YOEO_CLASSES
    )

    cap = cv2.VideoCapture(video_file)
    width = cap.get(cv2.CAP_PROP_FRAME_WIDTH)  # float
    height = cap.get(cv2.CAP_PROP_FRAME_HEIGHT)  # float
    origi_shape = (width, height)
    fps = round(cap.get(cv2.CAP_PROP_FPS))
    num_frames = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
    if verbose:
        print(num_frames, "frames detected!")

    orig_frames = []
    bbox_class_score = []

    vid_writer = cv2.VideoWriter(
        out_path, cv2.VideoWriter_fourcc(*"mp4v"), fps, (int(width), int(height))
    )

    index = 0
    start_time = time.time()
    while True:
        success, img = cap.read()
        index += 1
        if success:
            orig_frames.append(img)

            if index % (fps / ifps) == 0:  # inference optimization
                frame_start_time = time.time()
                outputs, img_info = predictor.inference(img.copy())
                result_frame, results = predictor.visual(
                    outputs[0], img_info, predictor.confthre
                )
                vid_writer.write(result_frame)
                ch = cv2.waitKey(1)
                if ch == 27 or ch == ord("q") or ch == ord("Q"):
                    break

                if verbose:
                    print(
                        "--- Frame inferred in %0.2f seconds ---"
                        % (time.time() - frame_start_time)
                    )

                bbox_class_score.append(results)

            else:
                vid_writer.write(img)

        if index > num_frames:
            break

    print("--- Completed in %0.2f seconds ---" % (time.time() - start_time))

    return (
        bbox_class_score,
        orig_frames,
        origi_shape,
        fps,
    )
